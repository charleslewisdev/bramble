async function seed() {
  console.log("Seeding plant reference database...");

  let inserted = 0;
  let updated = 0;
  for (const plant of samplePlants) {
    // Check if plant exists by common name AND latin name
    const existing = db
      .select({ id: plantReferences.id })
      .from(plantReferences)
      .where(
        and(
          eq(plantReferences.commonName, plant.commonName),
          plant.latinName
            ? eq(plantReferences.latinName, plant.latinName)
            : sql`${plantReferences.latinName} IS NULL`,
        ),
      )
      .get();

    if (existing) {
      // Update existing entry with new fields
      db.update(plantReferences)
        .set({ ...plant, updatedAt: new Date().toISOString() })
        .where(eq(plantReferences.id, existing.id))
        .run();
      updated++;
    } else {
      db.insert(plantReferences)
        .values(plant)
        .run();
      inserted++;
    }
  }

  console.log(
    `Seeded ${inserted} new, updated ${updated} existing plant references (${samplePlants.length} total in seed data).`,
  );
}

